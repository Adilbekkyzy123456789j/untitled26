import java.util.Arrays;

public    class  Animal {

    private int age;
    private int id;

    public Animal() {
    }

    public Animal(int age, int id) {
        this.age = age;
        this.id = id;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;

    }
}



